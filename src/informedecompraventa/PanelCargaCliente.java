    private boolean controlCarga(){
        boolean valido = true;
        if(jTextNombre.getText().equals("")){
            jLabelAviso.setVisible(true);
            jLabelNom.setForeground(Color.red);
            valido = false;
        }
        if(jTextApellido.getText().equals("")){
            jLabelAviso.setVisible(true);
            jLabelApel.setForeground(Color.red);
            valido = false;
        }
        if(jTextCuitCliente.getText().equals("")|| jTextCuitCliente.getText().length() != 11){
            jLabelCuit.setForeground(Color.red);
            valido = false;
        } else if (FramePrincipal.getTClientes().containsKey(jTextCuitCliente.getText()) && jCboxElegirCliente.getSelectedIndex()==0){
                jLabelCuit.setForeground(Color.red);
                jLabelEcuit.setVisible(true);
                valido = false;
            }
        if(jTeTelCliente.getText().equals("")){
            jLabelAviso.setVisible(true);
            jLabelTel.setForeground(Color.red);
            valido = false;
        }
        if(jCboxElegirCliente.getSelectedIndex()==1){
            if(jTextCuitComer.getText().equals("")|| jTextCuitComer.getText().length() != 11){
            jLabelCuitComer.setForeground(Color.red);
            valido = false;
            }else if (FramePrincipal.getTClientes().containsKey(jTextCuitComer.getText())){
                jLabelCuitComer.setForeground(Color.red);
                jLabelEcuit.setVisible(true);
                valido = false;
            }
            if(jTeTelComercio.getText().equals("")){
            jLabelTelCom.setForeground(Color.red);
            valido = false;
            jLabelAviso.setVisible(true);
            }
            if(jTextDireccion.getText().equals("")){
            jLabelDire.setForeground(Color.red);
            valido = false;
            jLabelAviso.setVisible(true);
            }
            if(jTextnombreComer.getText().equals("")){
            jLabel5NomC.setForeground(Color.red);
            valido = false;
            jTextnombreComer.setBackground(new java.awt.Color(255, 0, 0));
            jTextnombreComer.setHorizontalAlignment(javax.swing.JTextField.CENTER);
            jTextnombreComer.setBorder(javax.swing.BorderFactory.createLineBorder(new java.awt.Color(255, 0, 0)));
            jLabelAviso.setVisible(true);
            }            
        }
         return valido;
    }
