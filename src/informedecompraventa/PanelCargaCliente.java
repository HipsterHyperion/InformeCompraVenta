    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        resetLabels();
        if (JOptionPane.showConfirmDialog(null,"¿Esta seguro que desea cargar este cliente?")==0 && controlCarga()){
            if (jCboxElegirCliente.getSelectedIndex()==1){
                aux = (ClienteComercio) cargarClienteNuevo();
                FramePrincipal.getTClientes().put(aux.getCuitComercio(),aux);
            }else{
                FramePrincipal.getTClientes().put(cargarClienteNuevo().getCuit(),cargarClienteNuevo());
            }
            FramePrincipal.getEscritorio().removeAll();
            FramePrincipal.getEscritorio().repaint();
            VistaListaCliente vl= new VistaListaCliente(new PanelListaClientes());
            vl.setVisible(true);
            FramePrincipal.getEscritorio().add(vl);
            FramePrincipal.getEscritorio().moveToFront(vl);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

